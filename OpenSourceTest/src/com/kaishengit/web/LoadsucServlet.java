import java.io.IOException;
@WebServlet("/loadsuc")
public class LoadsucServlet extends HttpServlet {
    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        req.getRequestDispatcher("/WEB-INF/views/loadsuc.jsp").forward(req,resp);
    }
}
